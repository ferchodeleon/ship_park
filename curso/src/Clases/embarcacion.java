    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
        
        //Botón de insertar un dato nuevo
        
        //Primero valido que los datos no esten vacios
        if(jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty()|| jTextField3.getText().isEmpty()
           || jTextField4.getText().isEmpty()|| jTextField5.getText().isEmpty() || jTextField6.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "DATOS VACIOS", "Informacion", JOptionPane.INFORMATION_MESSAGE);
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
            jTextField1.requestFocus();
            jTextField2.requestFocus();
            jTextField3.requestFocus();
            jTextField4.requestFocus();
            jTextField5.requestFocus();
            jTextField6.requestFocus();
        } else {
            try {
                //Luego valido que el dato no exista en la base de datos
                res = Conexiones.Conexion.Consulta("Select ID_SOCIO from SOCIO where ID_SOCIO='" + jTextField5.getText() + "'");
                res1 = Conexiones.Conexion.Consulta("Select ID_AMARRE from AMARRE where ID_AMARRE='" + jTextField6.getText() + "'");
                try{
                    while(res.next()){
                        cont = res.getInt(1);
                    }
                }catch (SQLException  e){
                }
                try{
                    while(res1.next()){
                        cont1 =res1.getInt(1);
                    }
                }catch (SQLException  e){
                }
                //Si existe informo que el dato NO esta cargado
                if(cont <= 1){
                    JOptionPane.showMessageDialog(this, "EL NUMERO DE SOCIO NO EXISTE EN LA BASE DE DATOS\nCARGUE PRIMERO LOS DATOS EN LA TABLA SOCIO" , "Informacion", JOptionPane.INFORMATION_MESSAGE);
                }else if(cont1 <= 1){
                    JOptionPane.showMessageDialog(this, "EL NUMERO DE AMARRE NO EXISTE EN LA BASE DE DATOS\nCARGUE PRIMERO LOS DATOS EN LA TABLA AMARRE" , "Informacion", JOptionPane.INFORMATION_MESSAGE);
                //Si el dato ya existe cargo el dato he informo que se cargo OK
                }else {
                    Procedimientos.entradaEmbarcaciones(Integer.parseInt(jTextField1.getText().toString()),
                            jTextField2.getText(),
                            jTextField3.getText(),
                            Integer.parseInt(jTextField4.getText().toString()),
                            Integer.parseInt(jTextField5.getText().toString()),
                            Integer.parseInt(jTextField6.getText().toString()));
                    jTextField1.requestFocus();
                    jTextField2.requestFocus();
                    jTextField3.requestFocus();
                    jTextField4.requestFocus();
                    jTextField5.requestFocus();
                    jTextField6.requestFocus();
                    JOptionPane.showMessageDialog(this, "LOS DATOS HAN SIGO GUARDADOS CORRECTAMENTE");
                    cargarEmbarcacion();
                }
            }catch (SQLException e){
            }
        }
    }//GEN-LAST:event_jMenuItem3ActionPerformed

    private void jMenuItem4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem4ActionPerformed
        
        // PROCEDIMIENTO PARA ELIMINAR DATOS DE EMBARQUE
        
        int row = jTable1.getSelectedRow();
        // Pregunto si esta seguro de eliminar el dato
        int opc = JOptionPane.showConfirmDialog(this, "¿ESTAS SEGURO QUE DESEA ELIMINAR ESTE REGISTRO?","Pregunta",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        //Si se selecciona eliminar entra al procedimiento y elimina
        if(opc == JOptionPane.YES_OPTION){
            try{
                Procedimientos.eliminarEmbarques(Integer.parseInt(jTable1.getValueAt(row, 0).toString()));
                cargarEmbarcacion();
            }catch (SQLException e){
            }
        }
    }//GEN-LAST:event_jMenuItem4ActionPerformed

    private void jMenuItem5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem5ActionPerformed
        
        if(jTextField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "POR FAVOR VERIFIQUE LOS DATOS", "Error", JOptionPane.ERROR_MESSAGE);
            jTextField1.setText("");
            jTextField1.requestFocus();
        } else {
            try {
                String b;
                //Consulto por medio de un procedimiento almacenado
                Procedimientos.buscarEmbarques(Integer.parseInt(jTextField1.getText()));
                b = jTextField1.getText();
                jTextField1.setText("");
                jTextField1.requestFocus();
                res = Conexiones.Conexion.Consulta("select * from EMBARCACION");
                while(res.next()){
                    if(res.getString(1).equals(b)){
                        JOptionPane.showMessageDialog(null, "DATOS ENCONTRADOS");
                        jTextField1.setText(res.getString(1));
                        jTextField2.setText(res.getString(2));
                        jTextField3.setText(res.getString(3));
                        jTextField4.setText(res.getString(4));
                        jTextField5.setText(res.getString(5));
                        jTextField6.setText(res.getString(6));
                    }
                }
                JOptionPane.showMessageDialog(null, "DATOS NO ENCONTRADOS");
            }catch(SQLException e){
            }
        }
    }//GEN-LAST:event_jMenuItem5ActionPerformed

