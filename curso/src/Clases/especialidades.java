    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed

        //Primero valido que los datos no esten vacios
        if(jTextField2.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "DATOS VACIOS", "Informacion", JOptionPane.INFORMATION_MESSAGE);
            jTextField2.setText("");
            jTextField2.requestFocus();
        } else {
            try {
                //Luego valido que el dato no exista en la base de datos
                res = Conexiones.Conexion.Consulta("Select COUNT(NOMBRE_ESPECIALIDAD)from especialidades where NOMBRE_ESPECIALIDAD='" + jTextField2.getText() + "'");
                try{
                    while(res.next()){
                        cont = res.getInt(1);
                    }
                }catch (SQLException  e){
                }
                //Si existe informo que el dato ya esta cargado
                if(cont >= 1){
                    JOptionPane.showMessageDialog(this, "ESTE ELEMENTO YA EXISTE" , "Informacion", JOptionPane.INFORMATION_MESSAGE);
                //Si el dato no existe cargo el dato he informo que se cargo OK
                } else {
                    Procedimientos.entradaEspecialidades(jTextField2.getText());
                    jTextField2.setText("");
                    jTextField2.requestFocus();
                    JOptionPane.showMessageDialog(this, "LOS DATOS HAN SIGO GUARDADOS CORRECTAMENTE");
                    CargarEspecialidades();
                }
            }catch (SQLException e){
            }
        }
    }//GEN-LAST:event_jMenuItem3ActionPerformed

    private void jMenuItem4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem4ActionPerformed
        int row = jTable1.getSelectedRow();
        // Pregunto si esta seguro de eliminar el dato
        int opc = JOptionPane.showConfirmDialog(this, "¿ESTAS SEGURO QUE DESEA ELIMINAR ESTE REGISTRO?","Pregunta",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        //Si se selecciona eliminar entra al procedimiento y elimina
        if(opc == JOptionPane.YES_OPTION){
            try{
                Procedimientos.EliminarEspecialidades(Integer.parseInt(jTable1.getValueAt(row, 0).toString()));
                CargarEspecialidades();
            }catch (SQLException e){
            }
        }
    }//GEN-LAST:event_jMenuItem4ActionPerformed

    private void jMenuItem5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem5ActionPerformed
        
        if(jTextField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "POR FAVOR VERIFIQUE LOS DATOS", "Error", JOptionPane.ERROR_MESSAGE);
            jTextField1.setText("");
            jTextField1.requestFocus();
        } else {
            try {
                String b;
                //Consulto por medio de un procedimiento almacenado
                Procedimientos.BUSCAR_ESPECIALIDADES(Integer.parseInt(jTextField1.getText()));
                b = jTextField1.getText();
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField1.requestFocus();
                jTextField2.requestFocus();
                res = Conexiones.Conexion.Consulta("select * from ESPECIALIDADES");
                while(res.next()){
                    if(res.getString(1).equals(b)){
                        JOptionPane.showMessageDialog(null, "DATOS ENCONTRADOS");
                        jTextField1.setText(res.getString(1));
                        jTextField2.setText(res.getString(2));
                    }
                }
                JOptionPane.showMessageDialog(null, "DATOS NO ENCONTRADOS");
            }catch(SQLException e){
            }
        }
    }//GEN-LAST:event_jMenuItem5ActionPerformed

    private void jMenuItem6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem6ActionPerformed
        //Primero valido que los datos no esten vacios
        if(jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "DATOS VACIOS", "Informacion", JOptionPane.INFORMATION_MESSAGE);
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField1.requestFocus();
            jTextField2.requestFocus();
        } else {
            try {
                //Luego valido que el dato de empleado no exista en la base de datos
                res = Conexiones.Conexion.Consulta("Select ID_ESPECIALIDAD from ESPECIALIDADES where ID_ESPECIALIDAD='" + jTextField1.getText() + "'");
                try{
                    while(res.next()){
                        cont = res.getInt(1);
                    }
                }catch (SQLException  e){
                }
                //Si no existe informo que el dato no esta cargado
                if(cont <= 1){
                    JOptionPane.showMessageDialog(this, "LA ESPECIALIDAD NO EXISTE EN LA BASE DE DATOS\nINGRESE PRIMERO EL DATO EN LA TABLA ESPECIALIDAD" , "Informacion", JOptionPane.INFORMATION_MESSAGE);
                }else{
                    Procedimientos.ActualizarEspecialidades(Integer.parseInt(jTextField1.getText().toString()), jTextField2.getText());
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField1.requestFocus();
                    jTextField2.requestFocus();
                    JOptionPane.showMessageDialog(this, "LOS DATOS HAN SIGO GUARDADOS CORRECTAMENTE");
                    CargarEspecialidades();
                }
            }catch (SQLException e){
            }
        }
    }//GEN-LAST:event_jMenuItem6ActionPerformed

