    public DefaultTableModel CargarEspecialidades(){
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);
        try{
            CallableStatement miSentencia = (CallableStatement) Conexiones.Conexion.getConexion().prepareCall("{call CONSULTAS_ESPECIALIDADES()}");
            res = miSentencia.executeQuery();
            while(res.next()){
                Vector v = new Vector();
                v.add(res.getInt(1));
                v.add(res.getString(2));
                modelo.addRow(v);
                jTable1.setModel(modelo);
            }
        }catch (SQLException e){
            return null;
        }
        return modelo;
    }
    
    public static void Ingresar_especialidades(String a) throws SQLException{
          
            CallableStatement miSentencia = Conexiones.Conexion.getConexion().prepareCall("{call INSERTAR_DATOS_ESPECIALIDADES(?)}");
            //Cargar los datos en el procedimiento almacenado
            miSentencia.setString(1, a);
            miSentencia.execute();
            
    }
    
       
